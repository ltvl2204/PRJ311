    public String Category(int arr) {
        String a = null;
        switch (arr) {
            case 0:
                a = "PR";
                break;
            case 1:
                a = "LT";
                break;
            case 2:
                a = "CS";
                break;
            case 3:
                a = "TX";
                break;
            case 4:
                a = "RF";
                break;
            case 5:
                a = "MA";
                break;
        }
        return a;
    }

