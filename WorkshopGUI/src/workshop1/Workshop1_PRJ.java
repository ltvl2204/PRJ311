    public void reset()  {
        DefaultTableModel tableModel = (DefaultTableModel) this.jTable1.getModel();
        while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
        }
        list = BookDB.getlist();
        for (Book book : list) {
            tableModel.addRow(new Object[]{book.getBookID(), book.getTitle(), book.getAuthor(), book.getCategory(), book.getKeyword()});
        }
    }
