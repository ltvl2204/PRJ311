    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String bookID = jBookID.getText();
        String title = jTitle.getText();
        String author = jAuthor.getText();
        String category = Category(cbCategory.getSelectedIndex());
        String keyword = jKeyword.getText();
        if (!bookID.matches("^B\\d{4}$")) {
            JOptionPane.showMessageDialog(rootPane, "Book id has to right from B....");
        } else if (BookDB.getBook(bookID) != null) {
            JOptionPane.showMessageDialog(rootPane, "ID can not be blank");
        } else {
            Book book = new Book(bookID, title, author, category, keyword);
            BookDB.addNewBook(book);
            reset();
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) this.jTable1.getModel();
        String bookID = tableModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String title = jTitle.getText();
        String author = jAuthor.getText();
        String category = Category(cbCategory.getSelectedIndex());
        String keyword = jKeyword.getText();
        Book book = new Book(jBookID.getText(), title, author, category, keyword);
        System.out.println(book);
        BookDB.updateBook(bookID, book);
        reset();
    }//GEN-LAST:event_btnUpdateActionPerformed

